public class TestModifierWithPrivate {

	private void display() {
		System.out.println("Hello World");
	}
	
	public void displayCar() {
		System.out.println("Hello World");
	}	
	public void displayBus() {
		System.out.println("Hello World");
	}	
	
	protected void displayPlane() {
		System.out.println("Hello World");
	}	
	
	private void displayTruck() {
		System.out.println("Hello World");
	}	
		
	}
